public void getJumlah(){
        if( jumlah2.getSelectedItem().equals("2")){
                bil1.setVisible(true);
                bil2.setVisible(true);
                bil3.setVisible(false);
                bil4.setVisible(false);
                b1.setVisible(true);
                b2.setVisible(true);
                b3.setVisible(false);
                b4.setVisible(false);
                jmlh = 2;
        }
        else if( jumlah2.getSelectedItem().equals("3")){
                bil1.setVisible(true);
                bil2.setVisible(true);
                bil3.setVisible(true);
                bil4.setVisible(false);
                b1.setVisible(true);
                b2.setVisible(true);
                b3.setVisible(true);
                b4.setVisible(false);
                jmlh = 3;
        }
        else if( jumlah2.getSelectedItem().equals("4")){
                bil1.setVisible(true);
                bil2.setVisible(true);
                bil3.setVisible(true);
                bil4.setVisible(true);
                b1.setVisible(true);
                b2.setVisible(true);
                b3.setVisible(true);
                b4.setVisible(true);
                jmlh = 4;
        }
        else{
                bil1.setVisible(false);
                bil2.setVisible(false);
                bil3.setVisible(false);
                bil4.setVisible(false);
                b1.setVisible(false);
                b2.setVisible(false);
                b3.setVisible(false);
                b4.setVisible(false);
                
        }
    }
