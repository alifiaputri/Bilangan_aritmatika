    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        Double hsl;
        Double a1,a2,a3,a4;

        a1 = Double.parseDouble(bil1.getText().toString());
        a2 = Double.parseDouble(bil2.getText().toString());

        if(jns.equals("penjumlahan")){
            if(jmlh == 2){
                hsl = a1 + a2;
                hasil.setText(""+ a1 + " + " + a2 + " = " + hsl);
            }
            if(jmlh == 3){
                a3 = Double.parseDouble(bil3.getText().toString());
                hsl = a1 + a2 + a3;
                hasil.setText(""+ a1 + " + " + a2 + " + " + a3 + " = " + hsl);
            }
            if(jmlh == 4){
                a3 = Double.parseDouble(bil3.getText().toString());
                a4 = Double.parseDouble(bil4.getText().toString());
                hsl = a1 + a2 + a3 + a4;
                hasil.setText(""+ a1 + " + " + a2 + " + " + a3 + "+ " + a4 + " = " + hsl);
            }
        }
        else if(jns.equals("pengurangan")){
            if(jmlh == 2){
                hsl = a1 - a2;
                hasil.setText(""+ a1 + " - " + a2 + " = " + hsl);
            }
            if(jmlh == 3){
                a3 = Double.parseDouble(bil3.getText().toString());
                hsl = a1 - a2 - a3;
                hasil.setText(""+ a1 + " - " + a2 + " - " + a3 + " = " + hsl);
            }
            if(jmlh == 4){
                a3 = Double.parseDouble(bil3.getText().toString());
                a4 = Double.parseDouble(bil4.getText().toString());
                hsl = a1 - a2 - a3 - a4;
                hasil.setText(""+ a1 + " - " + a2 + " - " + a3 + " - " + a4 + " = " + hsl);
            }
        }
        else if(jns.equals("perkalian")){
            if(jmlh == 2){
                hsl = a1 * a2;
                hasil.setText(""+ a1 + " x " + a2 + " = " + hsl);
            }
            if(jmlh == 3){
                a3 = Double.parseDouble(bil3.getText().toString());
                hsl = a1 * a2 * a3;
                hasil.setText(""+ a1 + " x " + a2 + " x " + a3 + " = " + hsl);
            }
            if(jmlh == 4){
                a3 = Double.parseDouble(bil3.getText().toString());
                a4 = Double.parseDouble(bil4.getText().toString());
                hsl = a1 * a2 * a3 * a4;
                hasil.setText(""+ a1 + " x " + a2 + " x " + a3 + " x " + a4 + " = " + hsl);
            }
        }
        else if(jns.equals("pembagian")){
            if(jmlh == 2){
                hsl = a1 / a2;
                hasil.setText(""+ a1 + " / " + a2 + " = " + hsl);
            }
            if(jmlh == 3){
                a3 = Double.parseDouble(bil3.getText().toString());
                hsl = a1 / a2 / a3;
                hasil.setText(""+ a1 + " / " + a2 + " / " + a3 + " = " + hsl);
            }
            if(jmlh == 4){
                a3 = Double.parseDouble(bil3.getText().toString());
                a4 = Double.parseDouble(bil4.getText().toString());
                hsl = a1 / a2 / a3 / a4;
                hasil.setText(""+ a1 + " / " + a2 + " / " + a3 + " / " + a4 + " = " + hsl);
            }
        }
    }//GEN-LAST:event_hitungActionPerformed

